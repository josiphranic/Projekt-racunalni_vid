package fer.projekt.classes;

public class PositionVector {

	private double x;
	private double y;
	
	public PositionVector(double x, double y){
		this.x = x;
		this.y = y;
	}

	/**
	 * @return the x
	 */
	public double getX() {
		return x;
	}

	/**
	 * @return the y
	 */
	public double getY() {
		return y;
	}
	

}
